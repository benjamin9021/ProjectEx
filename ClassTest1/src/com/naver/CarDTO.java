package com.naver;

public class CarDTO {
	private String id;
	private String master;
	private String masterSsn;
	private String modelName;
	private String company;
	private String carStructureNum;
	private String fuel;
	private String carColor;
	private String carNum;
	
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMaster() {
		return master;
	}
	public void setMaster(String master) {
		this.master = master;
	}
	public String getMasterSsn() {
		return masterSsn;
	}
	public void setMasterSsn(String masterSsn) {
		this.masterSsn = masterSsn;
	}
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getCarStructureNum() {
		return carStructureNum;
	}
	public void setCarStructureNum(String carStructureNum) {
		this.carStructureNum = carStructureNum;
	}
	public String getFuel() {
		return fuel;
	}
	public void setFuel(String fuel) {
		this.fuel = fuel;
	}
	public String getCarColor() {
		return carColor;
	}
	public void setCarColor(String carColor) {
		this.carColor = carColor;
	}
	public String getCarNum() {
		return carNum;
	}
	public void setCarNum(String carNum) {
		this.carNum = carNum;
	}
	
}
